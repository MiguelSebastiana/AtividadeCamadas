package org.example.model;

public class Contato {
    
    private long id;
    private String nome;
    private String numero;

    public Contato(long id, String nome, String numero) {
        this.id = id;
        this.nome = nome;
        this.numero = numero;
    }

    public Contato(long id, String nome, String numero) {
        this.id = id;
        this.nome = nome;
        this.numero = numero;
    }
    
}
